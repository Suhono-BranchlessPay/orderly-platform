  const session = await patchSession(sessionId, {
    wizard: {
      ...prevWizard,
      catalog: nextCat,
      completedSteps: prevWizard.completedSteps || [],
    },
    currentStep: Math.max(existing.currentStep ?? 1, 5),
  });
  return { ok: true, session: session! };
