/**
 * Small fixed palette set — NOT generated by any model. Deterministic pick
 * keyed on a hash of the restaurant name so the same name always renders the
 * same stub theme (useful for demos + tests).
 */
const STUB_PALETTES: Array<{
  key: string;
  primary: string;
  accent: string;
}> = [
  { key: "ember", primary: "354 82% 50%", accent: "43 74% 49%" },
  { key: "sage", primary: "142 45% 36%", accent: "40 60% 55%" },
  { key: "ocean", primary: "205 70% 42%", accent: "190 55% 60%" },
  { key: "plum", primary: "280 45% 45%", accent: "330 55% 60%" },
  { key: "citrus", primary: "28 85% 50%", accent: "50 90% 55%" },
  { key: "slate", primary: "215 20% 35%", accent: "205 30% 55%" },
];

function nameHashInt(name: string): number {
  const hex = createHash("sha256").update(name.trim().toLowerCase()).digest("hex");
  return parseInt(hex.slice(0, 8), 16);
}

/** Stub auto theme — deterministic palette-from-name-hash, not ML. */
export function generateStubTheme(
  restaurantName: string,
  logoUrl?: string | null,
): Record<string, unknown> {
  const idx = nameHashInt(restaurantName) % STUB_PALETTES.length;
  const palette = STUB_PALETTES[idx];
  return {
    method: "stub_name_hash",
    paletteKey: palette.key,
    primary: palette.primary,
    accent: palette.accent,
    logoUrl: logoUrl?.trim() || null,
    brandName: restaurantName,
  };
}

